package Model;

public class Course {

    public String Cname;
    public String Cdurat;
    public String Cfee;
    public String Lecturer;
    public String Centre;

    public Course(String Cname, String Cdurat, String Cfee, String Lecturer, String Centre) {
        this.Cname = Cname;
        this.Cdurat = Cdurat;
        this.Cfee = Cfee;
        this.Lecturer = Lecturer;
        this.Centre = Centre;
    }

}
